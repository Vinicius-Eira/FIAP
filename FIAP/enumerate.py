'''
                     -> ENUMERATE <-

através do enumerate você encontra dentro de uma lista, quais elementos estão na
lista, mas também suas posições.

EXEMPLO:

'''


brinquedos = ['ursinho', 'carro', 'boneca', 'bola']

for indice, brinquedo in enumerate(brinquedos):
    print(f'No indíce{indice}, temos a(o) {brinquedo}')



'''
No indíce0, temos a(o) ursinho 
No indíce1, temos a(o) carro 
No indíce2, temos a(o) boneca 
No indíce3, temos a(o) bola 
'''